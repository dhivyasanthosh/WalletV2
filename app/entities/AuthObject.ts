export type SigninRequest = {
  email: string;
  password: string;
};
export type LoginResponse = {
  code: number;
  status: string;
  data: {tokens: TokenDetails};
  message: string;
  success?: boolean;
};

export type TokenDetails = {
  access: {
    token: string;
    expires: string;
  };
  refresh: {
    token: string;
    expires: string;
  };
};
